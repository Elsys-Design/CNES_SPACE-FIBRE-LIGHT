// procedures to override, read attribute values

task write_attr;
  input  [`PLLE4_ADV_ADDR_SZ-1:0] addr;
  input  [`PLLE4_ADV_DATA_SZ-1:0] data;
  begin
    ATTR[addr] = data;
    trig_attr = ~trig_attr; // to be removed
  end
endtask

function [`PLLE4_ADV_DATA_SZ-1:0] read_attr;
  input  [`PLLE4_ADV_ADDR_SZ-1:0] addr;
  begin
    read_attr = ATTR[addr];
  end
endfunction

task commit_attr;
  begin
trig_attr = ~trig_attr;
  end
endtask
